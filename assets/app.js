function applyTheme() {
  document.documentElement.dataset.theme = state.theme;
  localStorage.setItem('theme', state.theme);
}

const revealObserver = new IntersectionObserver(
  (entries) => {
    entries.forEach((entry) => {
      if (entry.isIntersecting) entry.target.classList.add('is-visible');
    });
  },
  { threshold: 0.12 },
);

function observeReveals() {
  const nodes = document.querySelectorAll('.reveal:not(.is-observed)');
  nodes.forEach((node) => {
    node.classList.add('is-observed');
    revealObserver.observe(node);
  });
}

document.querySelector('#languageToggle').addEventListener('click', () => {
  state.lang = state.lang === 'zh' ? 'en' : 'zh';
  applyLanguage();
});

document.querySelector('#themeToggle').addEventListener('click', () => {
  state.theme = state.theme === 'dark' ? 'light' : 'dark';
  applyTheme();
});

function animateField() {
  const canvas = document.querySelector('#field');
  const ctx = canvas.getContext('2d');
  const ratio = window.devicePixelRatio || 1;
  let width;
  let height;
  let points = [];

  function resize() {
    width = window.innerWidth;
    height = window.innerHeight;
    canvas.width = Math.floor(width * ratio);
    canvas.height = Math.floor(height * ratio);
    canvas.style.width = `${width}px`;
    canvas.style.height = `${height}px`;
    ctx.setTransform(ratio, 0, 0, ratio, 0, 0);
    const count = Math.min(48, Math.max(22, Math.floor(width / 34)));
    points = Array.from({ length: count }, () => ({
      x: Math.random() * width,
      y: Math.random() * height,
      vx: (Math.random() - 0.5) * 0.1,
      vy: (Math.random() - 0.5) * 0.1,
      r: 0.8 + Math.random() * 0.9,
    }));
  }

  function draw() {
    ctx.clearRect(0, 0, width, height);
    const isLight = document.documentElement.dataset.theme === 'light';
    ctx.fillStyle = isLight ? 'rgba(64, 74, 66, 0.16)' : 'rgba(231, 225, 213, 0.18)';
    ctx.strokeStyle = isLight ? 'rgba(64, 74, 66, 0.045)' : 'rgba(231, 225, 213, 0.055)';

    points.forEach((point) => {
      point.x += point.vx;
      point.y += point.vy;
      if (point.x < -20) point.x = width + 20;
      if (point.x > width + 20) point.x = -20;
      if (point.y < -20) point.y = height + 20;
      if (point.y > height + 20) point.y = -20;

      ctx.beginPath();
      ctx.arc(point.x, point.y, point.r, 0, Math.PI * 2);
      ctx.fill();
    });

    for (let i = 0; i < points.length; i += 1) {
      for (let j = i + 1; j < points.length; j += 1) {
        const dx = points[i].x - points[j].x;
        const dy = points[i].y - points[j].y;
        const distance = Math.hypot(dx, dy);
        if (distance < 115) {
          ctx.globalAlpha = (115 - distance) / 115;
          ctx.beginPath();
          ctx.moveTo(points[i].x, points[i].y);
          ctx.lineTo(points[j].x, points[j].y);
          ctx.stroke();
          ctx.globalAlpha = 1;
        }
      }
    }

    requestAnimationFrame(draw);
  }

  resize();
  window.addEventListener('resize', resize);
  draw();
}

